def _assert_geo_equal(a: object, b: object) -> None:
    if isinstance(a, dict) and isinstance(b, dict):
        da = cast(dict[str, Any], a)
        db = cast(dict[str, Any], b)
        assert set(da.keys()) == set(db.keys())
        for key in da:
            _assert_geo_equal(da[key], db[key])
        return
    if isinstance(a, list) and isinstance(b, list):
        assert len(a) == len(b)
        for x, y in zip(a, b):
            _assert_geo_equal(x, y)
        return
    if isinstance(a, (float, int)) and isinstance(b, (float, int)):
        af = float(a)
        bf = float(b)
        if math.isnan(af) and math.isnan(bf):
            return
        assert math.isclose(af, bf, rel_tol=0.0, abs_tol=1e-12)
        return
    assert a == b


